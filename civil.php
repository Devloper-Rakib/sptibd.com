function CodeCanalScrollFun() {
    if (document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) {
        document.getElementById("CCBtn").style.display = "block";
    } else {
        document.getElementById("CCBtn").style.display = "none";
    }
}

